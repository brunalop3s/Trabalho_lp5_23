        public FornecedorBlf viewBean() {
        FornecedorBlf fornecedorBlf = new FornecedorBlf();
        fornecedorBlf.setIdFornecedorBlf(Util.strInt(jTxtCodigo.getText()));
        fornecedorBlf.setEmpresaOuPessoaBlf(jTxtEmpresaPessoa.getText());
        fornecedorBlf.setCnpjBlf(jFmtCnpj.getText());
        fornecedorBlf.setTelefoneBlf(jFmtTelefone.getText());   
        fornecedorBlf.setEnderecoBlf(jTxtEndereco.getText());
        fornecedorBlf.setBairroBlf(jTxtBairro.getText());
        fornecedorBlf.setCidadeBlf(jTxtCidade.getText());
        fornecedorBlf.setUfBlf(jTxtUf.getText());
        fornecedorBlf.setCepBlf(jFmtCep.getText());
        fornecedorBlf.setEmailBlf(jTxtEmail.getText());
        
        return fornecedorBlf;
    }
      
       
