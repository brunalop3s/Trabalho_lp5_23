    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        // usuario ficou verde é o atributo da classe clobal
        fornecedorBlf = viewBean();

        if (incluindo == true) {
            fornecedor_DAO.insert(fornecedorBlf);
        } else {
            fornecedor_DAO.update(fornecedorBlf);
        }

         Util.habilitar(true, jTxtCodigo, jTxtEmpresaPessoa, jFmtCnpj,jFmtTelefone, jTxtBairro,
               jTxtEndereco, jFmtCep, jTxtCidade, jTxtUf, jTxtEmail, jBtnCancelar, jBtnConfirmar);
        Util.habilitar(false, jBtnIncluir, jBtnAlterar, jBtnExcluir, jBtnPesquisar);

        Util.limparCampos(jTxtCodigo, jTxtEmpresaPessoa, jFmtCnpj,jFmtTelefone, jTxtBairro, jTxtEndereco, jFmtCep, jTxtCidade, jTxtUf, jTxtEmail);

        Util.habilitar(false, jTxtCodigo, jTxtEmpresaPessoa, jFmtCnpj,jFmtTelefone, jTxtBairro,
               jTxtEndereco, jFmtCep, jTxtCidade, jTxtUf, jTxtEmail, jBtnCancelar, jBtnConfirmar);
        Util.habilitar(true, jBtnIncluir, jBtnAlterar, jBtnExcluir, jBtnPesquisar);

    }//GEN-LAST:event_jBtnConfirmarActionPerformed

