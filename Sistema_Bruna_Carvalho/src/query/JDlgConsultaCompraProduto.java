    private void jBtnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConsultarActionPerformed
       if(jTxtQuantidade.getText().equals("") && jTxtValorUnitario.getText().equals("")){
             List lista = compraProduto_DAO.listAll();
             compraProdutoControle.setList(lista);
              
                } else{
                     if(!jTxtQuantidade.getText().equals("") && !jTxtValorUnitario.getText().equals("")){
                     List lista = compraProduto_DAO.listQuantidadeValorUnitario(strInt(jTxtQuantidade.getText()), Util.strDouble(jTxtValorUnitario.getText()));
                     compraProdutoControle.setList(lista);
                
                    } else{
                         if(!jTxtQuantidade.getText().equals("")){
                         List lista = compraProduto_DAO.listQuantidade(Util.strInt(jTxtQuantidade.getText()));
                         compraProdutoControle.setList(lista);
                      
                        } else{
                         if(!jTxtValorUnitario.getText().equals("")){
                         List lista = compraProduto_DAO.listValorUnitario(Util.strDouble(jTxtValorUnitario.getText()));
                         compraProdutoControle.setList(lista);
                    }
                   }
                }    
            }  
    }//GEN-LAST:event_jBtnConsultarActionPerformed

